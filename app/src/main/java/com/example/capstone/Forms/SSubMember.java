package com.example.capstone.Forms;

public class SSubMember {
    private String product, manufacturer;
    private Integer cases_sold;

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public Integer getCases_sold() {
        return cases_sold;
    }

    public void setCases_sold(Integer cases_sold) {
        this.cases_sold = cases_sold;
    }
}
